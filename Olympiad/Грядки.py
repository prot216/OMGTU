# программа с циклом
   
l = 7;
m = 10;
n = 5;
k = int(input("введите кол-во грядок: "));
i = 1;
d = 0;
while i <= k:
    s = 2*l + 2*n + 2*i*m;
    d += s;
    i += 1;
print("S = ", d);

